"use client";

import { useState, useTransition } from "react";
import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import * as z from "zod";
import { requestPasswordResetAction } from "./actions";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import {
  Card,
  CardContent,
  CardDescription,
  CardFooter,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from "@/components/ui/form";
import Link from "next/link";

const forgotPasswordSchema = z.object({
  emailAddress: z.email("Please enter a valid email address"),
});

type ForgotPasswordFormData = z.infer<typeof forgotPasswordSchema>;

export function ForgotPasswordForm() {
  const [isPending, startTransition] = useTransition();
  const [serverError, setServerError] = useState<string | null>(null);
  const [success, setSuccess] = useState(false);

  const form = useForm<ForgotPasswordFormData>({
    resolver: zodResolver(forgotPasswordSchema),
    defaultValues: {
      emailAddress: "",
    },
  });

  const onSubmit = (data: ForgotPasswordFormData) => {
    setServerError(null);

    startTransition(async () => {
      const formData = new FormData();
      formData.append("emailAddress", data.emailAddress);

      const result = await requestPasswordResetAction(undefined, formData);
      if (result?.error) {
        setServerError(result.error);
      } else if (result?.success) {
        setSuccess(true);
      }
    });
  };

  if (success) {
    return (
      <Card>
        <CardHeader>
          <CardTitle>查看你的電郵</CardTitle>
          <CardDescription>
            如果該電郵有對應的帳戶，我們已經發送了重設密碼的指示。
          </CardDescription>
        </CardHeader>
        <CardFooter>
          <Link href="/sign-in">
            <Button variant="outline" className="w-full">
              返回登入
            </Button>
          </Link>
        </CardFooter>
      </Card>
    );
  }

  return (
    <Card>
      <CardHeader>
        <CardTitle>忘記密碼？</CardTitle>
        <CardDescription>
          輸入你的電郵地址，我們會發送一個連結讓你重設密碼。
        </CardDescription>
      </CardHeader>
      <Form {...form}>
        <form onSubmit={form.handleSubmit(onSubmit)}>
          <CardContent>
            <FormField
              control={form.control}
              name="emailAddress"
              render={({ field }) => (
                <FormItem>
                  <FormLabel>電郵地址</FormLabel>
                  <FormControl>
                    <Input
                      type="email"
                      placeholder="you@example.com"
                      disabled={isPending}
                      {...field}
                    />
                  </FormControl>
                  <FormMessage />
                </FormItem>
              )}
            />

            {serverError && (
              <div className="text-sm text-destructive mt-4">{serverError}</div>
            )}
          </CardContent>
          <CardFooter className="flex flex-col space-y-4 mt-4">
            <Button type="submit" className="w-full" disabled={isPending}>
              {isPending ? "Sending..." : "發送重設連結"}
            </Button>
            <Link
              href="/sign-in"
              className="text-sm text-center text-muted-foreground hover:text-primary"
            >
              返回登入
            </Link>
          </CardFooter>
        </form>
      </Form>
    </Card>
  );
}
